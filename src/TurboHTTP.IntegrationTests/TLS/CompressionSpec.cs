        for (var i = 0; i < body.Length; i++)
        {
            Assert.Equal((byte)('A' + i % 26), body[i]);
        }
    }

    [Fact(Timeout = 20000)]
    public async Task Brotli_response_should_be_transparently_decompressed_over_https()
